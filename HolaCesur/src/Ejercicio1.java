public class Ejercicio1 {
    public static void main(String[] args) {
    	// Inicializamos la variable con el valor inicial, que es 10
    	int numero = 10;
        // Utilizamos un bucle while para mostrar los números del 10 al 1
        while (numero >= 1) {
            System.out.println(numero);
            numero--; // Decrementamos la variable en cada iteración
        }
    }
}